package card;

/**
 * The four suits in a standard deck of 52 playing cards ordered alphabetically:
 *
 * <ol>
 * <li><code>CLUBS</code></li>
 * <li><code>DIAMONDS</code></li>
 * <li><code>HEARTS</code></li>
 * <li><code>SPADES</code></li>
 * </ol>
 */
public enum Suit {
    /**
     * The suit clubs
     */
    CLUBS,

    /**
     * The suit diamonds
     */
    DIAMONDS,

    /**
     * The suit hearts
     */
    HEARTS,

    /**
     * The suit spades
     */
    SPADES

}
